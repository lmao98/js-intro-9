// These are ++ or -- and used to increase or decrease value by 1

let n1 = 10,
  n2 = 10,
  n3 = 10,
  n4 = 10;

console.log(n1++); // 10 -> variable ++ is known as post-increment (it addes 1 in the next use)
console.log(++n2); // 11 -> variable is know as pre-increment (addes 1 right away)

let number = 5;

number++; // 5

--number; //6-1 = 5

number *= 2; // 5 * 2 = 10

console.log((++number) ** 2); // 11 ** 2 = 121
